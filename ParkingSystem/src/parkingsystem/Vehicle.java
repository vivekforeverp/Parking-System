package parkingsystem;

public class Vehicle {
	private int vehicle_type;
	private String manufacturer, car_number;
	private int model_number;
	/**
	 * @return the vehicle_type
	 */
	public int getVehicle_type() {
		return vehicle_type;
	}
	/**
	 * @param vehicle_type the vehicle_type to set
	 */
	public void setVehicle_type(int vehicle_type) {
		this.vehicle_type = vehicle_type;
	}
	/**
	 * @return the manufacturer
	 */
	public String getManufacturer() {
		return manufacturer;
	}
	/**
	 * @param manufacturer the manufacturer to set
	 */
	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}
	/**
	 * @return the car_number
	 */
	public String getCar_number() {
		return car_number;
	}
	/**
	 * @param car_number the car_number to set
	 */
	public void setCar_number(String car_number) {
		this.car_number = car_number;
	}
	/**
	 * @return the model_number
	 */
	public int getModel_number() {
		return model_number;
	}
	/**
	 * @param model_number the model_number to set
	 */
	public void setModel_number(int model_number) {
		this.model_number = model_number;
	}
	
	
}
